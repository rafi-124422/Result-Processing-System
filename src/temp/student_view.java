    private void show_result_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show_result_buttonActionPerformed
        // TODO add your handling code here:
       
        int sem  =   (int) select_semester_combo_box.getSelectedItem();
      
        if(sem==1)show_result_label.setText(String.format("Your %dst semester result is : ",sem));
        else if(sem==2)show_result_label.setText(String.format("Your %dnd semester result is : ",sem));
        else if(sem==3)show_result_label.setText(String.format("Your %drd semester result is : ",sem));
        else show_result_label.setText(String.format("Your %dth semester result is : ",sem));
        DefaultTableModel model = (DefaultTableModel)show_result_table.getModel(); 
        int rows = model.getRowCount(); 
        for(int i = rows - 1; i >=0; i--)
        {
           model.removeRow(i); 
        }
        try
            {
           
            Class.forName("oracle.jdbc.OracleDriver");
            Connection con = null;
            try {
		con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","124422");
                Statement state = con.createStatement();
                String query= String.format("select courseid,coursetitle,credit,gpa from course natural join marks where studentid='%s' and semesternumber=%d order by courseid",id,sem);
              
                ResultSet result=state.executeQuery(query);
          
                String courseid,ct,g;
                double gp,credit;
                double sem_gpa=-100,tot_credit=0,m = 0;
                 while(result.next())
                {
               
                   courseid = result.getString("courseId");
                   ct =  result.getString("CourseTitle");
                   credit = result.getDouble("credit");
                   gp = result.getDouble("gpa");
                   tot_credit+=credit;
                   if(gp>=4.00)g="    A+";
                   else if(gp>=3.75&&gp<4.00)g="    A";
                   else if(gp>=3.50&&gp<3.75)g="    A-";
                   else if(gp>=3.25&&gp<3.50)g="    B+";
                   else if(gp>=3.00&&gp<3.25)g="    B";
                   else if(gp>=2.75&&gp<3.00)g="    B-";
                   else if(gp>=2.50&&gp<2.75)g="    C+";
                   else if(gp>=2.25&&gp<2.50)g="    C";
                   else if(gp>=2.00&&gp<2.25)g="    D";
                   else g="    F";
                   model.addRow(new Object[]{courseid,ct,credit,gp,g});
                   m += (credit*gp); 
                  
                   if (gp==0)sem_gpa=0;
               
                }
                 result.close();
                 if(sem_gpa!=0)
                     sem_gpa = m/tot_credit;
                 show_sem_gpa_label.setText(String.format("Your GPA for this semester is %.3f", sem_gpa));
                show_sem_cgpa_label.setText(String.format("Your CGPA for this semester is %.3f",cgpa_cal(sem,id)));
           
		} 
            catch (SQLException e) 
            {
			// TODO Auto-generated catch block
                    JOptionPane.showMessageDialog(this,"error connect in query");
			e.printStackTrace();
            }       
        }
        catch(ClassNotFoundException e)
        {
             JOptionPane.showMessageDialog(this,"error ");
        }
        
    }//GEN-LAST:event_show_result_buttonActionPerformed

    private double cgpa_cal(int s,String sid)
    {
        double cgpa = 0;
        
        try
            {
           
            Class.forName("oracle.jdbc.OracleDriver");
            Connection con = null;
            try {
		con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","124422");
                
                for(int i = 1 ;i<=s;i++)
                {
                Statement state = con.createStatement();
                String query= String.format("select courseid,coursetitle,credit,gpa from course natural join marks where studentid='%s' and semesternumber=%d",sid,i);
              
                ResultSet result=state.executeQuery(query);
          
            
                double gp,credit;
                double sem_gpa=-100,tot_credit=0,m = 0;
                while(result.next())
                {
               
     
                   credit = result.getDouble("credit");
                   gp = result.getDouble("gpa");
                   tot_credit+=credit;
                   m += (credit*gp); 
                   //System.out.println(m+" "+tot_credit+" "+gp);
                   if (gp==0)sem_gpa=0;              
                }
                 result.close();
                 if(sem_gpa!=0)
                     sem_gpa = m/tot_credit;
                 cgpa+=sem_gpa;
		} 
            }
            catch (SQLException e) 
            {
			// TODO Auto-generated catch block
                    JOptionPane.showMessageDialog(this,"error connect in query");
			e.printStackTrace();
            }    
            
        }
        catch(ClassNotFoundException e)
        {
             JOptionPane.showMessageDialog(this,"error ");
        }
     
        return  cgpa/s;
    }
